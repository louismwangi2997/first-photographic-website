## Description

This project is a photography website designed to showcase a collection of favorite photos, including landscapes, city scenes, and everyday moments. The website highlights creativity through images while demonstrating the use of HTML and CSS for structuring and styling web pages. It is visually appealing and easy to navigate, providing users with an engaging browsing experience.

---

## setup

1. Download or clone the repository  
2. Open the project folder  
3. Locate the `index.html` file  
4. Double-click the file or open it in your web browser

To edit the project:
- Use any code editor like Visual Studio Code  
- Modify the `index.html` and `styles.css` files as needed  

